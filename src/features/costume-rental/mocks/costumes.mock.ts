const resolveBrandType = (index: number): BrandType => {
  const pick = index % 5
  if (pick === 0) return "tu_may"
  if (pick === 1) return "freestyle"
  if (pick === 2) return "non_brand"
  return "brand"
}

const resolveSizeOptions = (index: number): SizeKey[] => {
  const pick = index % 4
  if (pick === 0) return ["freesize"]
  if (pick === 1) return ["s", "m"]
  if (pick === 2) return ["m", "l", "xl"]
  return ["xs", "s", "m", "l"]
}

